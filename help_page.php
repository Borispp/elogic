					<div class="install_details questBlock -hide" id="ecology">
						<span class="detail__head">Ecologia</span>
						<p><strong>Calitatea apei în urma prelucrarii?</strong><br>— Este apă tehnică, mult mai curată decît multe rîuri(Nistru), iazuri(Ghidighici) și lacuri.</p>
						<p><strong>Ce putem face cu apa în urma prelucrarii?</strong><br>— Putem spăla pavajul, uda copacii și iarba sau folosi apa pentru alte scopuri tehnice, doar că nu se recomandă în alimentare.</p>
						<p><strong>Care este influența stației de epurare asupra solului?</strong><br>— Este benefică. Datorită acesteia, apele epurate nu provoacă focare de infecții. Și mai mult: apele epurate nu afectează apele subterane care ajung în fîntîni, la copaci, e.t.c. De la sol nu vor apărea mirosuri neplacute.</p>
						<p><strong>E posibil de sădit ceva deasupra instalației?</strong><br>— E posibil de sădit deasupra instalației absolut totul, drenajul este protejat de o membrană protectoare, dar se recomandă ca rădăcinile plantelor să nu depășească 60-70 cm. Acestea s-ar aduna în exces la sursa de apă, unele din radacinile copacilor ar putea cu timpul să spargă membrana protectoare și ar putea înfunda orificiile conductelor din drenaj.</p>
					</div>

					<div class="install_details questBlock -hide" id="install">
						<span class="detail__head">Instalarea</span>
						<p><strong>Marimea proiectului?</strong><br>— În dependență de volumul solicitat. Există soluții și pentru spații limitate.</p>
						<p><strong>Livrarea?</strong><br>— Este gratuită în municipiul Chișinău. În alte regiuni se calculează după kilometraj.</p>
						<p><strong>Montarea?</strong><br>— Gratuită.</p>
						<p><strong>Termenul montarii?</strong><br>— Împreună cu lucrările de excavare, livrare, astupare și curățenie - de la o săptămână la două. Dacă condițiile climaterice permit.</p>
						<p><strong>Regulamentul montarii?</strong><br>— Clientul trebuie să dispună de electricitate, apă, accesul lucrătorilor la locul montajului în orele de lucru, trebuie să asigure paza instrumentelor și materialelor.</p>
						<p><strong>Care este anotimpul potrivit pentru montarea proiectului?</strong><br>— Clima în Republica Moldova permite realizarea proiectului anul împrejur, cu condiția că nu se va lucra cu betonul la temperatură de sub minus 15 grade frig. Ecologic are soluții și tehnologii pentru perioada rece.</p>
					</div>

					<div class="install_details questBlock -hide" id="care">
						<span class="detail__head">Îngrijirea</span>
						<p><strong>Termenul de exploatare?</strong><br>— Sute de ani, cu vidanjări periodice, schimb de filtru și profilaxii.</p>
						<p><strong>Cum să am grija?</strong><br>— Ecologic nu are nevoie de îngrijiri, doar de vidanjări o dată la 3-10 ani(în dependență de sistemul procurat).</p>
						<p><strong>Ce este interzis?</strong><br>— Aruncarea în canalizare: gunoiul din construcții, cantități mari de păr uman și de animale, pungi de polietelena și alte materiale care greu se descompun în natură.</p>
						<p><strong>Ce produse chimice se permit pentru folosire și care nu se permit?</strong><br>— Se permite spalarea vaselor și hainelor cu detergenții obișnuiți, spălarea cu săpun și șampon. Nu se permite aruncarea în canalizare a clorului concentrat, dizolvanților, benzinei, spirtului, antibioticele. Mini-stația este ca organismul uman, nu iubeste în exces: aciditate, chimie și alte produse agresive.</p>
            <p><strong>Cum se produce vidanjarea stației?</strong><br>— Vidanjarea stației se efectuiază cu o mașină specializată care vine la programarea clientului, ea curata totul iarăși pentru o perioadă de 3-10 ani în dependență de sistemul propus.</p>
            <p><strong>Instucțiuni de autodeservire?</strong><br>— Găsim reviziile de la stație (5 cm. sub pamînt), le scoatem capacele, conectăm furtunul pînă la fund și îl tragem 5 cm. Începem vidanjarea la toate trei camere. E de dorit să umplem cu apă stația și să producem încă o dată vidanjarea, după care umplem stația cu apă. Ecologic este proiectată să se repornească singură, fără ajutorul bioactivatorilor. Instrucțiunile sunt scrise în detalii și în pașaportul tehnic.</p>
					</div>

					<div class="install_details questBlock -hide" id="except">
						<span class="detail__head">Cazuri excepționale</span>
						<p><strong>Ce facem în caz că nu este lumină?</strong><br>— Toate pachete de tip - nisip nu au nevoie de energie electrică. Pachetele de tip - argilă nisipoasă Full și Econom nu au nevoie de energie electrică.</p>
						<p><strong>Ce facem în caz de inundație?</strong><br>— În cazul inundațiilor dacă apele terestre nu depășesc 10 cm. înălțime nu trebuie să ne facem griji pentru că sistema va continua să lucreze. Iar în cazul în care au depașit pragul de 10 cm. trebuie să facem o chemare pentru verificarea statiei sau facem acest lucru singuri. Găsim în pașaportul tehnic.</p>
						<p><strong>Ce facem în caz de înghețuri mari?</strong><br>— Dacă înghețurile sunt pînă la -20 grade/C starea este bună, pînă la -25 grade/C starea este normală, pînă la -35 grade/C procesele lucrează mai lent, ar fi bine să aruncam o pastilă de bioactivator pentru a intensifica procesele.</p>
						<p><strong>Ce facem în caz de cutremur de pămînt?</strong><br>— În cazul unui cutremur de pămînt Complexul Ecologic este protejat pentru că este proiectat și montat în așa mod ca să nu de sufere în urma cutremurului. (Dar după aceasta sunînd la serviciul suport tehnic se poate verifica starea sistemei).</p>
            <p><strong>Cum procedez dacă apa nu se duce?</strong><br>— Dacă a fost verificată conducta pînă la ministație sau fosă și nu s-au produs schimbări atunci a venit timpul pentru a produce vidanjarea stației. Acesta și este indiciul că stația are nevoie de curățare.</p>
