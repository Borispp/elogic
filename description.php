					<div class="description__text">
						<p class="description__head-text">"EcoLogic" este o statie de epurare anaerob-aeroba locala de prelucrare biologica a apelor reziduale menajere de la case individuale, vile, cabane, baruri, restaurante.</p>
						<p>Secretul perfectiunii sale este in simplitate si fabilitate - nu se strica. In ea nu are ce sa strice. Metoda anaerob-aeroba de epurare a apelor menajere, folosita in sistema in sistema EcoLogic, este primara de numit "clasica". Aceaste metoda de epurare este considerata si in continuare cea mai fiabila din toate cee cunoscute. Instalatia "EcLogic" consta dintr-un modul cu patru dispartiuri, acestea fiind trei decantoare-digetoare si un filtru biologic integrate intr-un carcas monolit din beton armat termoizolat pe exterior.</p>
						<p>Instalatia data prezinta o constructie in care intr-un singur volum se produce simultan atit decantarea apei, cit si fermentarea namolului rezultat din sedimentare. Este o instalatie de tratament biologic natural, in care se petrece pastrarea indelungata si putrfactia sedimentelor asezate. Aici se petrece procesul de degradare biologica a substantelor organice din namoluri, rin activitatea unor populatii bacteriene, care in anumite cinditii de mediu (pH, temperatura, etc), descompun materiile organice din namol prin procese de oxido - reducere biochimica in molucule simole de CH4, CO, CO2, si H2, care formeaza asa nummitul gaz de fermentatie sau biogaz.</p>
						<p>Cinetica fermentarii anaerobe se desfasoara sub influenta a doua grupe principale de bacterii care traiesc in simbioza in acelasi mediu fizic si chimic respectiv bacterii anaerobe si aerobe, care transforma, prin hidroliza, sustantele organice complexe (hidratii de carbon, proteine grasimi) in sustante organice mai simple (acizi organici inferiori, alcooli etc.) cu ajutorul enzimelor extracelulare. In continuare aceste sustante sint sursa de hrana pentru moleculele mai simple, care au ajutorul enzimelor intracelulare sint transformate in compusi simpli si inofensevi mediului: metan, dioxid de carbon etc. Sedimentele se vidanjeaza o data la 3-5 ani cu ajutorul unor utilaje speciale si se inlocuieste materialul filtrat cu unul nou. Dupa fiecare vidanjare se lasa in decantoare o cantitate de namol vechi, adica un namol ce contine bacterii metanice necesare pentru fermentarea namolului proaspat ce urmeaza a fi mineralizat.</p>

						<blockquote>
